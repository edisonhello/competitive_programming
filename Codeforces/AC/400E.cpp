#define int unsigned long long
int a[100005];
struct node{
    node *l,*r;
    int llen[20],rlen[20],insv[20];
    ll len;
    node():l(0),r(0){
        MS(llen,0); MS(rlen,0); MS(insv,0);
        len=0;
    }
} *root;
#define mid ((l+r)>>1)
ll calc(ll nn){return nn*(nn+1)/2;}
void pull(node *now){
    for(int i=0;i<20;++i){
        bool gap=1;
        if(now->l->llen[i]==now->l->len)now->llen[i]=now->l->len+now->r->llen[i],gap=0;
        else now->llen[i]=now->l->llen[i];
        if(now->r->rlen[i]==now->r->len)now->rlen[i]=now->r->len+now->l->rlen[i],gap=0;
        else now->rlen[i]=now->r->rlen[i];
        now->insv[i]=now->l->insv[i]+now->r->insv[i];
        if(gap)now->insv[i]+=calc(now->l->rlen[i]+now->r->llen[i]);
        // if(gap)PDE4(i,now->len,now->l->rlen[i],now->r->llen[i]);
    }
}
void init(node *now,int l,int r){
    if(l==r){
        now->len=1;
        for(int i=0;i<20;++i)now->llen[i]=now->rlen[i]=!!(a[l]&(1<<i));
        return;
    }
    init(now->l=new node(),l,mid);
    init(now->r=new node(),mid+1,r);
    now->len=r-l+1; pull(now);
}
void modify(node *now,int l,int r,int p,int x){
    if(l==r){
        a[p]=x;
        for(int i=0;i<20;++i)now->llen[i]=now->rlen[i]=!!(a[l]&(1<<i));
        return;
    }
    if(p<=mid)modify(now->l,l,mid,p,x);
    else modify(now->r,mid+1,r,p,x);
    pull(now);
}
