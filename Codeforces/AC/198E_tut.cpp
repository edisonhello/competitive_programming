void query(int x,int p,queue<int> &q){
    while(x>0){
        PDE2(x,p);
        while(BIT[x].size() && BIT[x].top().first<=p){
            if(!u[BIT[x].top().second])q.push(BIT[x].top().second),u[BIT[x].top().second]=1;
            BIT[x].pop();
        }
        PDE1(q);
        x-=lowbit(x);
    }
}
