    long long n,a,b,k; cin>>n>>a>>b>>k;
    fac[0]=1;
    for(int i=1;i<=n;++i)fac[i]=fac[i-1]*i%mod;
    ifac[n]=pw(fac[n],mod-2,mod);
    for(int i=n-1;i>=0;--i)ifac[i]=ifac[i+1]*(i+1)%mod;
