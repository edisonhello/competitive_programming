long long fac[300005],ifac[300005];

long long pw(long long b,long long n,long long m,long long a=1){
    while(n){
        if(n&1)a=a*b%m;
        b=b*b%m; n>>=1;
    } return a;
}
long long C(int n,int i){
    return ifac[i]*ifac[n-i]%mod*fac[n]%mod;
}

