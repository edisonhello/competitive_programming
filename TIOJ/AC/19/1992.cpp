void dfs(int n,int m){
PDE2(n,m);
    if(m==0)return;
    if(cf[n][m])return;

    tc[n][m].first=(1ll<<62);
    for(int i=1;i<=min(n,m);++i){
        int nn=n-i,mm=m;
        if(mm>=nn){
            swap(nn,mm);
            if(!cf[nn][mm])dfs(nn,mm);
            if(tc[nn][mm].second+f[i]<=tc[n][m].first){
                tc[n][m]=tc[nn][mm];
                swap(tc[n][m].first,tc[n][m].second);
                tc[n][m].first+=f[i];
                cf[n][m]=i;
            }
        }
        else{
            if(!cf[nn][mm])dfs(nn,mm);
            if(tc[nn][mm].first+f[i]<=tc[n][m].first){
                tc[n][m]=tc[nn][mm];
                tc[n][m].first+=f[i];
                cf[n][m]=i;
            }
        }
    }
}
void trace(int n,int m,int &win){
    if(m==0)return;
    take.push_back(cf[n][m]);
    n-=cf[n][m];
    if(m>=n)win^=1,swap(n,m);
    trace(n,m,win);
}

